// AND OPERATOR (&&):

var value = 10
var isvalue = value >=7 && value <=10;    //left sa chk hoga  //left and right sa true hui to true hogs //left sa false hui to false hoga 
console.log(isvalue);


// OR OPERATOR (||):

var value = 10
var isvalue = value >=7 || value <=10;   // dono ma sa ek bhi sahi hui to true hojaega
console.log(isvalue);